/**
 * Convert scripts/pats-waiver-scrape output (pats_waivers.csv) into D1 SQL
 * for the Vehicle ETIC waiver table (after migrations through 0019).
 *
 * - Prefixes reg_no with AF for asset_id when uploading to the metric
 *   AF##B01234 style roster (skips if reg_no already starts with AF).
 * - Imports only PATS rows whose legacy status is "Outstanding" (active
 *   card); Fixed/Deleted/Repainted are skipped (see stderr summary).
 * - Uses import_key = 'PATS:<waiver_row_id>' for idempotent re-run.
 *
 * Usage:
 *   node pats-to-d1-sql.mjs path/to/pats_waivers.csv path/to/out.sql
 *
 * Apply (remote example):
 *   npx wrangler d1 execute etic-snapshots --remote --file=path/to/out.sql
 */

import fs from "node:fs";

const DEFAULT_CARD_STATUS = "Outstanding";

function sqlStr(s) {
  return `'${String(s ?? "").replace(/'/g, "''")}'`;
}

/** @param {string} raw MM/DD/YYYY or variants */
function toIso(raw) {
  const t = (raw ?? "").trim();
  const m = t.match(/^(\d{1,2})\/(\d{1,2})\/(\d{2,4})\s*$/);
  if (!m) return null;
  let mm = Number(m[1]);
  let dd = Number(m[2]);
  let yy = Number(m[3]);
  if (yy < 100) yy += yy >= 50 ? 1900 : 2000;
  if (!Number.isFinite(mm) || !Number.isFinite(dd) || mm < 1 || mm > 12 || dd < 1 || dd > 31)
    return null;
  const iso = `${String(yy).padStart(4, "0")}-${String(mm).padStart(2, "0")}-${String(dd).padStart(2, "0")}T12:00:00.000Z`;
  return iso;
}

function assetIdFromReg(regNo) {
  const r = (regNo ?? "").trim().replace(/\s+/g, "");
  if (!r) return "";
  if (/^AF/i.test(r)) return r;
  return `AF${r}`;
}

function parseCsvLine(line) {
  const out = [];
  let cur = "";
  let inQ = false;
  for (let i = 0; i < line.length; i++) {
    const c = line[i];
    if (inQ) {
      if (c === '"') {
        if (line[i + 1] === '"') {
          cur += '"';
          i++;
        } else inQ = false;
      } else cur += c;
    } else {
      if (c === ",") {
        out.push(cur);
        cur = "";
      } else if (c === '"') inQ = true;
      else cur += c;
    }
  }
  out.push(cur);
  return out;
}

function main() {
  const inPath = process.argv[2] || "pats_waivers.csv";
  const outPath = process.argv[3] || "pats_waiver_import.sql";
  const body = fs.readFileSync(inPath, "utf8");
  const lines = body.split(/\r?\n/).filter(Boolean);
  if (lines.length < 2) {
    console.error("CSV needs a header and at least one row");
    process.exit(1);
  }
  const header = parseCsvLine(lines[0]).map((h) => h.trim().toLowerCase());
  const idx = (name) => header.indexOf(name);

  const iVehicle = idx("vehicle_id");
  const iReg = idx("reg_no");
  const iRow = idx("waiver_row_id");
  const iText = idx("waiver_text");
  const iEmp = idx("employee_no");
  const iTime = idx("time_added");
  const iStatus = idx("status");
  if (iReg < 0 || iRow < 0 || iText < 0) {
    console.error("CSV must include columns: reg_no, waiver_row_id, waiver_text");
    process.exit(1);
  }

  let used = 0;
  let skippedStatus = 0;
  let skippedDup = new Set();
  const importKeys = new Set();

  const chunks = [];
  chunks.push(
    "-- Generated by pats-to-d1-sql.mjs — apply after migrations/0019_waiver_import_key.sql\n" +
      "PRAGMA foreign_keys = ON;\n",
  );

  for (let li = 1; li < lines.length; li++) {
    const cols = parseCsvLine(lines[li]);
    const get = (i) => (i >= 0 ? (cols[i] ?? "").trim() : "");

    const regNo = get(iReg);
    const waiverRowId = get(iRow);
    const waiverText = get(iText);
    const emp = get(iEmp);
    const timeAdded = get(iTime);
    const legacyStatus = get(iStatus) || DEFAULT_CARD_STATUS;

    if (!waiverRowId || !waiverText) continue;

    if (legacyStatus.trim().toLowerCase() !== "outstanding") {
      skippedStatus++;
      continue;
    }

    const importKey = `PATS:${waiverRowId}`;
    if (importKeys.has(importKey)) continue;
    importKeys.add(importKey);

    const assetId = assetIdFromReg(regNo);
    if (!assetId) continue;

    const title =
      waiverText.length <= 120 ? waiverText : `${waiverText.slice(0, 117)}...`;
    const iso = toIso(timeAdded) || new Date().toISOString();
    // Description is only the defect text shown in the app; traceability stays in import_key.
    const desc = waiverText;

    const submittedBy = emp || "legacy import";
    const reviewedBy = "legacy import";
    const reviewedNote = "Imported from NEI PATS.";

    chunks.push(
      `INSERT INTO waiver (asset_id, title, description, status, submitted_by, submitted_at_iso, reviewed_by, reviewed_at_iso, reviewed_note, last_verified_by, last_verified_at_iso, photo_r2_key, photo_content_type, import_key)
SELECT ${sqlStr(assetId)}, ${sqlStr(title)}, ${sqlStr(desc)}, 'approved', ${sqlStr(submittedBy)}, ${sqlStr(iso)}, ${sqlStr(reviewedBy)}, ${sqlStr(iso)}, ${sqlStr(reviewedNote)}, ${sqlStr(reviewedBy)}, ${sqlStr(iso)}, NULL, NULL, ${sqlStr(importKey)}
WHERE NOT EXISTS (SELECT 1 FROM waiver WHERE import_key = ${sqlStr(importKey)});`,
    );

    chunks.push(
      `INSERT INTO waiver_verification (waiver_id, verified_by, verified_at_iso, note, kind)
SELECT w.id, ${sqlStr(reviewedBy)}, ${sqlStr(iso)}, NULL, 'initial' FROM waiver w
 WHERE w.import_key = ${sqlStr(importKey)}
   AND NOT EXISTS (SELECT 1 FROM waiver_verification v WHERE v.waiver_id = w.id AND v.kind = 'initial');\n`,
    );
    used++;
  }

  fs.writeFileSync(outPath, chunks.join("\n") + "\n", "utf8");
  console.error(
    `Wrote ${used} waiver INSERT blocks to ${outPath} (${importKeys.size} unique import_keys). Skipped ${skippedStatus} non-Outstanding rows.`,
  );
}

main();
